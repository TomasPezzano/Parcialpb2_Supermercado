package ar.edu.unlam.pb;

public class Caja {
	
	private Double dinero;
	private Integer id;
	
	public Caja() {
		this.dinero=0.0;
	}

	public Double getDinero() {
		return dinero;
	}

	public void setDinero(Double dinero) {
		this.dinero = dinero;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

}
